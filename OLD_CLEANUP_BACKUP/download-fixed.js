export default async function handler(req, res) {
    console.log('=== Download Request ===');
    console.log('Method:', req.method);
    console.log('Query:', req.query);
    console.log('Body:', req.body);

    // CORSË®≠ÂÆö
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

    if (req.method === 'OPTIONS') {
        return res.status(200).end();
    }

    // GET„É™„ÇØ„Ç®„Çπ„Éà: „ÉÄ„Ç¶„É≥„É≠„Éº„Éâ„Éö„Éº„Ç∏„ÇíË°®Á§∫
    if (req.method === 'GET') {
        const { id } = req.query;
        
        if (!id) {
            return res.status(400).send('„Éï„Ç°„Ç§„É´ID„ÅåÊåáÂÆö„Åï„Çå„Å¶„ÅÑ„Åæ„Åõ„Çì');
        }

        // „ÉÄ„Ç¶„É≥„É≠„Éº„Éâ„Éö„Éº„Ç∏„ÅÆHTML„ÇíËøî„Åô
        res.setHeader('Content-Type', 'text/html; charset=utf-8');
        return res.status(200).send(getDownloadPageHTML(id));
    }

    // POST„É™„ÇØ„Ç®„Çπ„Éà: „Éï„Ç°„Ç§„É´„ÉÄ„Ç¶„É≥„É≠„Éº„ÉâÂá¶ÁêÜ
    if (req.method === 'POST') {
        try {
            const { fileId, securityCode } = req.body;

            if (!fileId || !securityCode) {
                return res.status(400).json({ 
                    error: '„Éï„Ç°„Ç§„É´ID„Å®„Çª„Ç≠„É•„É™„ÉÜ„Ç£„Ç≥„Éº„Éâ„ÅåÂøÖË¶Å„Åß„Åô' 
                });
            }

            // „É°„Çø„Éá„Éº„Çø„Éï„Ç°„Ç§„É´„ÅÆ„Éë„Çπ„ÇíÊßãÁØâ
            const metadataPath = path.join(STORAGE_DIR, `${fileId}.meta.json`);
            const metadataPath2 = path.join(STORAGE_DIR, `${fileId}.json`);

            // „É°„Çø„Éá„Éº„Çø„ÇíË™≠„ÅøËæº„ÇÄ
            let metadata;
            if (fs.existsSync(metadataPath)) {
                metadata = JSON.parse(fs.readFileSync(metadataPath, 'utf8'));
            } else if (fs.existsSync(metadataPath2)) {
                metadata = JSON.parse(fs.readFileSync(metadataPath2, 'utf8'));
            } else {
                console.log('„É°„Çø„Éá„Éº„Çø„Éï„Ç°„Ç§„É´„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì:', fileId);
                return res.status(404).json({ 
                    error: '„Éï„Ç°„Ç§„É´„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì' 
                });
            }

            // „Çª„Ç≠„É•„É™„ÉÜ„Ç£„Ç≥„Éº„Éâ„ÅÆÊ§úË®º
            if (metadata.securityCode !== securityCode) {
                console.log('„Çª„Ç≠„É•„É™„ÉÜ„Ç£„Ç≥„Éº„Éâ‰∏ç‰∏ÄËá¥:', {
                    expected: metadata.securityCode,
                    received: securityCode
                });
                return res.status(401).json({ 
                    error: '„Çª„Ç≠„É•„É™„ÉÜ„Ç£„Ç≥„Éº„Éâ„ÅåÊ≠£„Åó„Åè„ÅÇ„Çä„Åæ„Åõ„Çì' 
                });
            }

            // „ÉÄ„Ç¶„É≥„É≠„Éº„ÉâÂõûÊï∞„ÅÆ„ÉÅ„Çß„ÉÉ„ÇØ
            const maxDownloads = metadata.maxDownloads || 5;
            if (metadata.downloadCount >= maxDownloads) {
                return res.status(403).json({ 
                    error: `„ÉÄ„Ç¶„É≥„É≠„Éº„ÉâÂõûÊï∞„ÅÆ‰∏äÈôêÔºà${maxDownloads}ÂõûÔºâ„Å´ÈÅî„Åó„Åæ„Åó„Åü` 
                });
            }

            // „Éï„Ç°„Ç§„É´„ÅÆÂ≠òÂú®Á¢∫Ë™ç
            const filePath = path.join(STORAGE_DIR, metadata.fileName);
            if (!fs.existsSync(filePath)) {
                console.log('„Éï„Ç°„Ç§„É´„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì:', filePath);
                return res.status(404).json({ 
                    error: '„Éï„Ç°„Ç§„É´„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì' 
                });
            }

            // „ÉÄ„Ç¶„É≥„É≠„Éº„ÉâÂõûÊï∞„ÇíÂ¢ó„ÇÑ„Åô
            metadata.downloadCount = (metadata.downloadCount || 0) + 1;
            metadata.lastDownloadedAt = new Date().toISOString();
            
            // „É°„Çø„Éá„Éº„Çø„ÇíÊõ¥Êñ∞
            if (fs.existsSync(metadataPath)) {
                fs.writeFileSync(metadataPath, JSON.stringify(metadata, null, 2));
            } else {
                fs.writeFileSync(metadataPath2, JSON.stringify(metadata, null, 2));
            }

            console.log(`„ÉÄ„Ç¶„É≥„É≠„Éº„ÉâÊàêÂäü: ${metadata.originalName} (${metadata.downloadCount}/${maxDownloads})`);

            // „Éï„Ç°„Ç§„É´„ÇíÈÄÅ‰ø°
            const fileContent = fs.readFileSync(filePath);
            res.setHeader('Content-Type', metadata.mimeType || 'application/octet-stream');
            res.setHeader('Content-Disposition', `attachment; filename="${metadata.originalName}"`);
            res.setHeader('Content-Length', fileContent.length);
            
            return res.status(200).send(fileContent);

        } catch (error) {
            console.error('„ÉÄ„Ç¶„É≥„É≠„Éº„Éâ„Ç®„É©„Éº:', error);
            return res.status(500).json({ 
                error: '„Çµ„Éº„Éê„Éº„Ç®„É©„Éº„ÅåÁô∫Áîü„Åó„Åæ„Åó„Åü' 
            });
        }
    }

    // „Åù„ÅÆ‰ªñ„ÅÆ„É°„ÇΩ„ÉÉ„Éâ„ÅØË®±ÂèØ„Åó„Å™„ÅÑ
    return res.status(405).json({ 
        error: 'Method not allowed' 
    });
}
